def palindrome_substrings(str)
  result = []
  substrings_arr = substrings(str)
  substrings_arr.each do |substring|
    result << substring if is_palindrome?(substring)
  end 
  result 
end 

def is_palindrome?(str)
  str == str.reverse
end 

